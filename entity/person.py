class Person:
    def __init__(self, data):
        self.name = data['name']
        self.height = data['height']
        self.mass = data['mass']
        self.hair_color = data['hair_color']
        self.skin_color = data['skin_color']
        self.eye_color = data['eye_color']
        self.birth_year = data['birth_year']
        self.gender = data['gender']
